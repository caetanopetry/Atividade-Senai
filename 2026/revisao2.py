# Dia de semana:

print("Dias da semana de 1 á 7")
dia_semana = int (input("Qual dia da semana você quer?: "))

if dia_semana == 1:
    print("Domingo")

elif dia_semana == 2:
    print("Segunda-Feira")

elif dia_semana == 3:
    print("Terça-Feira")

elif dia_semana == 4:
    print("Quarta-Feira")

elif dia_semana == 5:
    print("Quinta-Feira")

elif dia_semana == 6:
    print("Sexta-Feira")

elif dia_semana == 7:
    print("Sábado")

else:
    print("Número digitado inválido!")